/** Variable 선언
 * 1) var - 더이상 사용하지 않음, 변경 가능
 * 2) let - 변경 가능
 * 3) const - 변경 불가
 */

var name = "Eric";
console.log(name);

var age = 27;
console.log(age);

let ive = "아이브";
console.log(ive);

ive = "장원영";
console.log(ive);

const newJeans = "뉴진스";
console.log(newJeans);

// newJeans = "하니";

let girlFriend;
console.log(girlFriend);

